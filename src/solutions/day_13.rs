fn is_reflection_vert(idx: usize, grid: &Vec<Vec<char>>) -> usize {
    let mut top = idx;
    let mut bottom = idx + 1;
    if bottom == grid.len() {
        return 0;
    }
    while bottom < grid.len() {
        for col in 0..grid[0].len() {
            if grid[top][col] != grid[bottom][col] {
                return 0;
            }
        }
        if top == 0 {
            break;
        }
        top -= 1;
        bottom += 1;
    }
    (idx + 1) * 100
}

fn is_reflection(idx: usize, grid: &Vec<Vec<char>>) -> usize {
    let mut left = idx;
    let mut right = left + 1;
    if right == grid[0].len() {
        return 0;
    }
    while right < grid[0].len() {
        for row in 0..grid.len() {
            if grid[row][left] != grid[row][right] {
                return 0;
            }
        }
        if left == 0 {
            break;
        }
        left -= 1;
        right += 1;
    }
    idx + 1
}

fn score_(grid: &Vec<Vec<char>>, score_to_avoid: usize) -> usize {
    let mut ret;
    for row_idx in 0..grid[0].len() {
        ret = is_reflection(row_idx, &grid).max(0);
        if ret > 0 && ret != score_to_avoid {
            return ret;
        }
    }
    for col_idx in 0..grid.len() {
        ret = is_reflection_vert(col_idx, &grid).max(0);
        if ret > 0 && ret != score_to_avoid {
            return ret;
        }
    }
    0
}

fn score(grid: &str) -> (usize, usize) {
    let mut grid: Vec<Vec<char>> = grid
        .lines()
        .map(|l| l.chars().collect::<Vec<char>>())
        .collect();
    let p1_score = score_(&grid, 0);
    for r in 0..grid.len() {
        for c in 0..grid[0].len() {
            let old_char = grid[r][c];
            if old_char == '.' {
                grid[r][c] = '#';
            } else {
                grid[r][c] = '.';
            }
            let score = score_(&grid, p1_score);
            if score > 0 {
                return (p1_score, score);
            }
            grid[r][c] = old_char;
        }
    }
    (p1_score, 0)
}

